    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String insertTableSQL = "UPDATE stock SET  minvolume = ? WHERE bloodtype ='"+comBloodType.getSelectedItem()+"' AND rhd='"+comRHD.getSelectedItem()+"'";
                 
            PreparedStatement preparedStatement = DB.getconn().prepareStatement(insertTableSQL);
           // preparedStatement.setString(1, ID);
            preparedStatement.setString(1, txtVolume.getText());
           
            preparedStatement.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Update completed !"); 
        comBloodType.setSelectedIndex(0);
        comRHD.setSelectedIndex(0);
        txtVolume.setText("");
        getMinStockLevels();
      
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(rootPane, "Saving error !");
          
        }   
    }//GEN-LAST:event_jButton1ActionPerformed

