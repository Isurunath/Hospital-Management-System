private void getMinStockLevels() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) tbl_stock_level.getModel();
            dtm.setRowCount(0);
            ResultSet rs = new Stock().getStock();
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("bloodtype"));
                v.add(rs.getString("rhd"));
              
                v.add(rs.getString("minvolume"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
        }

    }



