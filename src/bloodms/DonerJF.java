    private void Doner_Register_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Doner_Register_SaveActionPerformed
        String Tp = Doner_Register_Tp.getText();
        if (Tp.length() == 10)
        {
        
        String ID = Doner_Register_id.getText();
        String Name = Doner_Register_Name.getText();
        String age = Doner_Register_Age.getText();
        String weight = Doner_Register_Weight.getText();
        String Address = Doner_Register_Address.getText();
        //String Tp = Doner_Register_Tp.getText();
        String Sex = Doner_Register_Sex.getSelectedItem().toString();
        String BloodType = Doner_Register_BloodType.getSelectedItem().toString();
        String RHD = Doner_Register_RHD.getSelectedItem().toString();
        if (Doner_Register_Save.getText().equals("Save")) {
            String msg = new Doner().saveDoner(ID, Name, age, weight, Address, Tp, Sex, BloodType, RHD);
            JOptionPane.showMessageDialog(rootPane, msg);

        } else {
            String msg = new Doner().updateDoner(ID, Name, age, weight, Address, Tp, Sex, BloodType, RHD);
            JOptionPane.showMessageDialog(rootPane, msg);

            if (msg.equals("Update completed !")) {
                DonerReset();
            }
        }
        
        }
        else{ 
           JOptionPane.showMessageDialog(null, "Enter correct Telephone details","",JOptionPane.ERROR_MESSAGE);
                
        }
        
    }//GEN-LAST:event_Doner_Register_SaveActionPerformed

