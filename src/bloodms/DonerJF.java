    private void Doner_Register_idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Doner_Register_idKeyReleased
        if (evt.getKeyCode() != 10) {
            String ID = Doner_Register_id.getText();
            if (ID.length() == 10) {
                try {
                    ResultSet rs = new Doner().getDoner(ID);
                    while (rs.next()) {
                        JOptionPane.showMessageDialog(rootPane, "Doner already exits!.Click to load");
                        Doner_Register_Save.setText("Update");
                        Doner_Register_id.setText(rs.getString("id"));
                        Doner_Register_Name.setText(rs.getString("name"));
                        Doner_Register_Age.setText(rs.getString("age"));
                        Doner_Register_Weight.setText(rs.getString("weight"));
                        Doner_Register_Address.setText(rs.getString("address"));
                        Doner_Register_Tp.setText(rs.getString("tp"));
                        Doner_Register_Sex.setSelectedItem(rs.getString("sex"));
                        Doner_Register_BloodType.setSelectedItem(rs.getString("bloodtype"));
                        Doner_Register_RHD.setSelectedItem(rs.getString("rhd"));
                    }
                } catch (Exception e) {
                    System.out.println(e);
                }

            }
        }
    }//GEN-LAST:event_Doner_Register_idKeyReleased

