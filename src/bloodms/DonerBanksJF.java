    private void btnBloodBankActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBloodBankActionPerformed
       String Tp = txtTp.getText();
        if (Tp.length() == 10)
        {
        String name, hospital, address, tp, bloodtype, rhd;
        name = txtName.getText();
        hospital = txtHospital.getText();
        address = txtAddress.getText();
        tp = txtTp.getText();
        bloodtype = comBloodType.getSelectedItem().toString();
        rhd = comRHD.getSelectedItem().toString();
         if (btnBloodBank.getText().equals("Save")) {
        String msg = new DonerBank().saveDonerBank(name, hospital, address, tp, bloodtype, rhd);
        JOptionPane.showMessageDialog(rootPane, msg);
         }else{
              String msg = new DonerBank().updateDonerBank(name, hospital, address, tp, bloodtype, rhd);
            JOptionPane.showMessageDialog(rootPane, msg);

            if (msg.equals("Update completed !")) {
                DonerBankReset();
            }
         }
        }
           else{ 
           JOptionPane.showMessageDialog(null, "Enter correct Telephone details","",JOptionPane.ERROR_MESSAGE);
                
        }
    }//GEN-LAST:event_btnBloodBankActionPerformed

