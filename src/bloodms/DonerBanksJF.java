    private void txtNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNameKeyReleased
       boolean row=false;
        if (evt.getKeyCode() == 10) {
            String ID = txtName.getText();
           
                try {
                    ResultSet rs = new DonerBank().getDonerBank(ID);
                    
                    while (rs.next()) {
                        row=true;
                      //  JOptionPane.showMessageDialog(rootPane, "Doner Bank already exits!.Click to load");
                        btnBloodBank.setText("Update");
                        txtName.setText(rs.getString("name"));
                        txtHospital.setText(rs.getString("hospital"));
                        txtAddress.setText(rs.getString("address"));
                        txtTp.setText(rs.getString("tp"));
                        comBloodType.setSelectedItem(rs.getString("bloodtype"));                       
                        comRHD.setSelectedItem(rs.getString("rhd"));
                    }
                    if(!false){
                        txtHospital.grabFocus();
                        JOptionPane.showMessageDialog(null, "Doner Bank not found! click Ok"); 
                    }
                } catch (Exception e) {
                    System.out.println(e);
                }

          
        }
    }//GEN-LAST:event_txtNameKeyReleased

