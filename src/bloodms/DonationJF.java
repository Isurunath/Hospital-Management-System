    private void txtIdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIdKeyReleased
       boolean row=false;
        if (evt.getKeyCode() == 10) {
            if (comtype.getSelectedItem().toString().equals("Person")) {
                try {
                    ResultSet rs = new Doner().getDoner(txtId.getText());
                    while (rs.next()) {
                       row=true;
                        comBloodType.setSelectedItem(rs.getString("bloodtype"));
                        comRHD.setSelectedItem(rs.getString("rhd"));
                    }
                    if (!row) {
                      JOptionPane.showMessageDialog(rootPane, "Data not found");
                    }

                } catch (Exception e) {
                    System.out.println(e);
                }

            } else {
                new DonerBank().getDonerBank(txtId.getText());
            }

        }
    }//GEN-LAST:event_txtIdKeyReleased

