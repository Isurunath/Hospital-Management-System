    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String ID = txtId.getText();
        String volume = txtVolume.getText();
        String RHD = comRHD.getSelectedItem().toString();
        String BloodType = comBloodType.getSelectedItem().toString();
        String Type = comtype.getSelectedItem().toString();
        String msg = new BloodReceived().saveGRN(ID, volume, BloodType, RHD, Type);
        new Stock().updateStock(ID, volume, BloodType, RHD, Type);
        if (msg.equals("Save completed !")) {
            JOptionPane.showMessageDialog(rootPane, msg);
            ClearDonation();
        } else {
            JOptionPane.showMessageDialog(rootPane, msg);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

