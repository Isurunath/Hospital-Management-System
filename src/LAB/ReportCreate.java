    private void Print_infoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Print_infoActionPerformed
       try{
           
           boolean complete=contest.print();
           
           if(complete){
             JOptionPane.showMessageDialog(null,"Done Printing","information",JOptionPane.INFORMATION_MESSAGE);
               
           }
           else{
               
              JOptionPane.showMessageDialog(null,"Printing","information",JOptionPane.ERROR_MESSAGE);  
           }
           
           
       }catch(PrinterException e){
         
          JOptionPane.showMessageDialog(null , e); 
           
           
       }
        
        
    }//GEN-LAST:event_Print_infoActionPerformed

